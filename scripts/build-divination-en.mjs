/**
 * Generate src/data/divinationSticks.en.ts
 * Run: node scripts/build-divination-en.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { POLISH_EN } from './divination-polish-en/sticks.mjs'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const OUT = path.join(__dirname, '../src/data/divinationSticks.en.ts')

const STICK_IDS = Array.from({ length: 100 }, (_, i) => i + 1)

function esc(str) {
  return str.replace(/\\/g, '\\\\').replace(/'/g, "\\'")
}

function emitEntry(id, p) {
  let o = `  ${id}: {
    titleEn: '${esc(p.titleEn)}',
    levelEn: '${esc(p.levelEn)}',
    plainPoemEn: '${esc(p.plainPoemEn)}',
    interpretationEn: '${esc(p.interpretationEn)}',
    adviceEn: '${esc(p.adviceEn)}',`

  if (p.storyEn) {
    o += `\n    storyEn: '${esc(p.storyEn)}',`
  }

  o += `\n    detailsEn: {`
  for (const [k, v] of Object.entries(p.detailsEn)) {
    o += `\n      ${k}: '${esc(v)}',`
  }
  o += `\n    },`

  if (p.ageEn) {
    o += `\n    ageEn: {`
    for (const [k, v] of Object.entries(p.ageEn)) {
      o += `\n      ${k}: '${esc(v)}',`
    }
    o += `\n    },`
  }

  o += `\n  }`
  return o
}

for (const id of STICK_IDS) {
  if (!POLISH_EN[id]) {
    throw new Error(`Missing English content for stick id ${id}`)
  }
}

const header = `// Divination stick English locale data (generated by scripts/build-divination-en.mjs)
export interface DivinationStickLocale {
  titleEn: string
  levelEn: string
  plainPoemEn: string
  interpretationEn: string
  adviceEn: string
  storyEn?: string
  detailsEn?: {
    home?: string
    business?: string
    travel?: string
    marriage?: string
    wealth?: string
    health?: string
    lawsuit?: string
    lostItem?: string
    searchPerson?: string
    relocation?: string
    career?: string
    pregnancy?: string
    livestock?: string
    disputes?: string
    illness?: string
    transaction?: string
    traveler?: string
  }
  ageEn?: {
    child?: string
    youngGirl?: string
    youngBoy?: string
    male?: string
    female?: string
  }
}

export const divinationSticksEn: Record<number, DivinationStickLocale> = {
`

const body = STICK_IDS.map((id) => emitEntry(id, POLISH_EN[id])).join(',\n')
const footer = '\n}\n'

fs.writeFileSync(OUT, header + body + footer, 'utf8')
console.log(`Wrote ${STICK_IDS.length} English stick entries to ${OUT}`)
