/**
 * Generate src/data/tarotCards.en.ts
 * Run: node scripts/build-tarot-en.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { POLISH_MAJOR_EN } from './tarot-polish-en/major.mjs'
import { POLISH_WANDS_EN } from './tarot-polish-en/wands.mjs'
import { POLISH_CUPS_EN } from './tarot-polish-en/cups.mjs'
import { POLISH_SWORDS_EN } from './tarot-polish-en/swords.mjs'
import { POLISH_PENTACLES_EN } from './tarot-polish-en/pentacles.mjs'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const OUT = path.join(__dirname, '../src/data/tarotCards.en.ts')

const POLISH = {
  ...POLISH_MAJOR_EN,
  ...POLISH_WANDS_EN,
  ...POLISH_CUPS_EN,
  ...POLISH_SWORDS_EN,
  ...POLISH_PENTACLES_EN,
}

function esc(str) {
  return str.replace(/\\/g, '\\\\').replace(/'/g, "\\'")
}

function emitCategories(cats) {
  let o = '    categories: {\n'
  for (const [k, v] of Object.entries(cats)) {
    o += `      ${k}: {\n`
    o += `        upright: '${esc(v.upright)}',\n`
    o += `        reversed: '${esc(v.reversed)}',\n`
    o += `      },\n`
  }
  o += '    },\n'
  return o
}

function emitCard(id, p) {
  let o = `  ${id}: {\n`
  o += `    meaning: {\n`
  o += `      upright: '${esc(p.meaning.upright)}',\n`
  o += `      reversed: '${esc(p.meaning.reversed)}',\n`
  o += `    },\n`
  o += `    description: '${esc(p.description)}',\n`
  o += `    interpretation: {\n`
  o += `      upright: '${esc(p.interpretation.upright)}',\n`
  o += `      reversed: '${esc(p.interpretation.reversed)}',\n`
  o += `    },\n`
  o += `    advice: {\n`
  o += `      upright: '${esc(p.advice.upright)}',\n`
  o += `      reversed: '${esc(p.advice.reversed)}',\n`
  o += `    },\n`
  o += emitCategories(p.categories)
  o += `  }`
  return o
}

const ids = Object.keys(POLISH)
  .map(Number)
  .sort((a, b) => a - b)

for (let i = 0; i <= 77; i++) {
  if (!POLISH[i]) {
    throw new Error(`Missing English polish for card id ${i}`)
  }
}

const header = `// Tarot card locale data (English) — generated by scripts/build-tarot-en.mjs
export interface TarotCategoryReading {
  upright: string
  reversed: string
}

export interface TarotCardLocale {
  meaning: {
    upright: string
    reversed: string
  }
  description: string
  interpretation: {
    upright: string
    reversed: string
  }
  advice: {
    upright: string
    reversed: string
  }
  categories: {
    love: TarotCategoryReading
    career: TarotCategoryReading
    wealth: TarotCategoryReading
    health: TarotCategoryReading
  }
}

export const tarotCardsEn: Record<number, TarotCardLocale> = {
`

const body = ids.map((id) => emitCard(id, POLISH[id])).join(',\n')
const footer = '\n}\n'

fs.writeFileSync(OUT, header + body + footer, 'utf8')
console.log(`Wrote ${ids.length} cards to ${OUT}`)
