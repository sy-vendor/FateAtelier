/**
 * Generate src/data/dreamSymbols.en.ts
 * Run: node scripts/build-dream-en.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { POLISH_ANIMALS_NATURE } from './dream-polish/animals-nature.mjs'
import { POLISH_PEOPLE_BUILDING } from './dream-polish/people-building.mjs'
import { POLISH_ITEMS_ACTIONS } from './dream-polish/items-actions.mjs'
import { POLISH_ANIMALS_NATURE_EN } from './dream-polish-en/animals-nature.mjs'
import { POLISH_PEOPLE_BUILDING_EN } from './dream-polish-en/people-building-en.mjs'
import { POLISH_ITEMS_ACTIONS_EN } from './dream-polish-en/items-actions-en.mjs'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const OUT = path.join(__dirname, '../src/data/dreamSymbols.en.ts')

const SOURCE_ZH = [
  ...POLISH_ANIMALS_NATURE,
  ...POLISH_PEOPLE_BUILDING,
  ...POLISH_ITEMS_ACTIONS,
]

const SOURCE_EN = [
  ...POLISH_ANIMALS_NATURE_EN,
  ...POLISH_PEOPLE_BUILDING_EN,
  ...POLISH_ITEMS_ACTIONS_EN,
]

function esc(str) {
  return str.replace(/\\/g, '\\\\').replace(/'/g, "\\'")
}

function emitSymbol(s) {
  const kw = s.keywords.map((k) => `'${esc(k)}'`).join(', ')
  const themes = s.themesEn.map((t) => `'${esc(t)}'`).join(', ')
  return `  {
    keywords: [${kw}],
    category: '${esc(s.category)}',
    categoryKey: '${esc(s.categoryKey)}',
    meaningEn: '${esc(s.meaningEn)}',
    interpretationEn: '${esc(s.interpretationEn)}',
    positiveEn: '${esc(s.positiveEn)}',
    negativeEn: '${esc(s.negativeEn)}',
    adviceEn: '${esc(s.adviceEn)}',
    themesEn: [${themes}],
  }`
}

if (SOURCE_ZH.length !== SOURCE_EN.length) {
  throw new Error(`English symbol count ${SOURCE_EN.length} does not match Chinese source count ${SOURCE_ZH.length}`)
}

SOURCE_EN.forEach((symbol, index) => {
  const zh = SOURCE_ZH[index]
  if (!Array.isArray(symbol.keywords) || symbol.keywords.length === 0) {
    throw new Error(`Missing keywords for English symbol at index ${index}`)
  }
  if (symbol.categoryKey !== zh.category) {
    throw new Error(`categoryKey mismatch at index ${index}: expected ${zh.category}, got ${symbol.categoryKey}`)
  }
})

const header = `// Dream symbol locale data (English) — generated by scripts/build-dream-en.mjs
export interface DreamSymbolLocale {
  keywords: string[]
  category: string
  categoryKey: string
  meaningEn: string
  interpretationEn: string
  positiveEn: string
  negativeEn: string
  adviceEn: string
  themesEn: string[]
}

export const dreamSymbolsEn: DreamSymbolLocale[] = [
`

const body = SOURCE_EN.map(emitSymbol).join(',\n')
const footer = '\n]\n'

fs.writeFileSync(OUT, header + body + footer, 'utf8')
console.log(`Wrote ${SOURCE_EN.length} English dream symbols to ${OUT}`)
