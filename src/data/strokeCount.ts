// 获取字符的笔画数（如果不在数据库中，返回估算值）
export function getStrokeCount(char: string): number {
  if (strokeCount[char]) {
    return strokeCount[char]
  }
  
  // 如果不在数据库中，使用简单的估算方法
  // 基于Unicode范围估算（不准确，但可以作为备选）
  const code = char.charCodeAt(0)
  if (code >= 0x4e00 && code <= 0x9fff) {
    // CJK统一汉字，返回一个估算值（基于常见笔画数）
    return 10 // 默认返回10画
  }
  
  return 0
}

// 获取字符串的总笔画数
export function getTotalStrokeCount(text: string): number {
  return text.split('').reduce((sum, char) => sum + getStrokeCount(char), 0)
}
